import UIKit

extension UIColor {
    static var customLightGray: UIColor {
        let color = UIColor(red: 240/255, green: 240/255, blue: 240/255, alpha: 1)
        return color
    }
    
    static var customLightGreen: UIColor {
        let color = UIColor(red: 112/255, green: 191/255, blue: 113/255, alpha: 1)
        return color
    }
}
